  const file = join(localeDir, `${lang}.json`)
  const json = JSON.parse(readFileSync(file, 'utf8'))
  let added = 0
  for (const [key, value] of Object.entries(entries)) {
    if (json[key] !== value) added++
    json[key] = value
  }
  writeFileSync(file, JSON.stringify(json, null, 2) + '\n', 'utf8')
